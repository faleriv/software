    private void cl_jTF_nombreEmpresaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cl_jTF_nombreEmpresaKeyReleased
        // TODO add your handling code here:
        Funciones fun = new Funciones();
        if(evt.getKeyCode() ==KeyEvent.VK_ENTER)
        {    if (fun.validarNombreEmpresa(cl_jTF_nombreEmpresa.getText()) == false) 
            {    
                this.bandera =false;
                JOptionPane.showMessageDialog(null, "Error, nombre empresa tiene más de 50 caracteres o tiene caracteres que no son alfabéticos ");
           
            }
            else
            {
                cl_jTF_nombreRepresentante.requestFocus();
                this.bandera =true;
            }
        }
    }//GEN-LAST:event_cl_jTF_nombreEmpresaKeyReleased

