    private void inicializar(int valor)
    {
        //1 --> registrar
        //2 --> actualizar
        switch (valor)
        {
            case 1:
                setTitle("SIGEB | Registro Clientes"); //Poner el título
                this.cl_jL_actualizarCliente.setVisible(false);
                this.cl_jTB_actualizar.setVisible(false);
                this.cl_jCB_estado.setSelectedIndex(0);
                this.cl_jCB_estado.setEnabled(false);
                break;
            case 2:
                setTitle("SIGEB | Actualización Cliente");
                this.cl_jL_registroCliente.setVisible(false);
                this.cl_jL_actualizarCliente.setVisible(true);
                
                this.cl_jTF_nombreEmpresa.setEditable(false);
                this.cl_jTF_nombreRepresentante.setEditable(false);
                this.cl_jTF_ruc.setEditable(false);
                this.cl_jTF_apellidosRepresentante.setEditable(false);
                this.cl_jB_registrar.setVisible(false);
                this.cl_jCB_estado.setEnabled(false);
                break;
        
        }
    }
    
