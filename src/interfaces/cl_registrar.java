    private void cl_jB_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cl_jB_registrarActionPerformed
        // TODO add your handling code here:
        //Registrar cliente
        if(bandera)
        {
            
            Procedimientos pro = new Procedimientos();
            
            if (pro.consultarSiExiste("ruc", "cliente", cl_jTF_ruc.getText()))
            {
                JOptionPane.showMessageDialog(null, "Ya existe un usuario con ese RUC, no se registró ningún cliente");
            }
            else
            {
               String arr[] = new String[10];
               arr[0]=cl_jTF_ruc.getText();
               arr[1]=cl_jTF_nombreEmpresa.getText();
               arr[2]=cl_jTF_nombreRepresentante.getText();
               arr[3]=cl_jTF_apellidosRepresentante.getText();
               arr[4]=cl_jTF_telefono.getText();
               arr[5]=cl_jTF_telefonocelular.getText();
               arr[6]=cl_jCB_provincia.getSelectedItem().toString();
               arr[7]=cl_jTF_ciudad.getText();
               arr[8]=cl_jTF_parroquia .getText();   
               arr[9]=cl_jCB_estado.getSelectedItem().toString();
              
               String cad = "RegistroCliente";
                try {
                    Procedimientos.registro(arr,cad);
                    JOptionPane.showMessageDialog(null, "Cliente Registrado exitosamente");
                } catch (SQLException ex) {
                    Logger.getLogger(cl_registrar.class.getName()).log(Level.SEVERE, null, ex);
                }
               
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Alguno de los campos ingresados son erróneos, No se registro el cliente");
        }
    }//GEN-LAST:event_cl_jB_registrarActionPerformed

