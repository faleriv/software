    private void fecha_jtfKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fecha_jtfKeyReleased
        // TODO add your handling code here:
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            boolean resultado = fu.validacionFecha(this.fecha_jtf.getText());
            if (!resultado) {
                JOptionPane.showMessageDialog(null, "La fecha ingresada es incorrecta\nIngresela nuevamente.");
                this.fecha_jtf.setText("");
                bandera = false;
            } else {
                bandera = true;
                costo_jtf.requestFocus();
            }
        }
        
    }//GEN-LAST:event_fecha_jtfKeyReleased

    private void costo_jtfKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_costo_jtfKeyReleased
        // TODO add your handling code here:
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            boolean resultado = fu.validacionCostoMensual(this.costo_jtf.getText());
            if (!resultado) {
                JOptionPane.showMessageDialog(null, "El valor mensual ingresado es incorrecta\nIngresela nuevamente.");
                this.costo_jtf.setText("");
                bandera = false;
            } else {
                bandera = true;
                duracion_jtf.requestFocus();
            }
        }
        
    }//GEN-LAST:event_costo_jtfKeyReleased

    private void duracion_jtfKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_duracion_jtfKeyReleased
        // TODO add your handling code here:
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            boolean resultado = fu.validacionDuracionContrato(this.duracion_jtf.getText());
            if (!resultado) {
                JOptionPane.showMessageDialog(null, "El valor ingresado no cumple el formato establecido\nIngresela nuevamente.");
                this.duracion_jtf.setText("");
                bandera = false;
            } else {
                bandera = true;
                jToggleButton2.requestFocus();
            }
        }
        
    }//GEN-LAST:event_duracion_jtfKeyReleased

