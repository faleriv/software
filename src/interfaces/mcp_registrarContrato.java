    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        // TODO add your handling code here:
        //varaibles
        if (!fecha_jtf.getText().isEmpty() && !costo_jtf.getText().isEmpty() && !duracion_jtf.getText().isEmpty()) {
            if (bandera) {
                alb.fecha = fecha_jtf.getText();
                alb.precio = costo_jtf.getText();
                alb.meses = duracion_jtf.getText();
                alb.ruc = mcp_jTF_ruc.getText();

                //pasar a otra ventana
                alb.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Alguno de los campos esta incorrecto, corrija porfavor");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Alguno de los campos esta vacio, corrija porfavor");
        }
    }//GEN-LAST:event_jToggleButton2ActionPerformed

